    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)tblFlightInfo.getModel();
        dtm.setRowCount(0);
        SimpleDateFormat sdf =  new SimpleDateFormat( " yyyy-MM-dd " );
        
        for(Flight a : airliner.getFlightSchedule().getFlightList()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a;
            row[1] = a.getDepartureCity();
            row[2] = a.getArrivingCity();
            row[3] = sdf.format(a.getDepartureTime());
            row[4] = sdf.format(a.getArrivingTime());
            row[5] = a.getTimeOfDay();
            dtm.addRow(row);
        }
    }
