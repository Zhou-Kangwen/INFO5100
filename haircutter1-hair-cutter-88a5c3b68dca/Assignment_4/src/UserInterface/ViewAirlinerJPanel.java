    private void btnUpdateFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateFlightActionPerformed
        // TODO add your handling code here:
          int selectedRow = tblFlightInfo.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first to update", "warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Flight account = (Flight)tblFlightInfo.getValueAt(selectedRow, 0);
            UpdateJPanel jpanel = new UpdateJPanel(rightJPanel,account);
            rightJPanel.add("UpdateJPanel",jpanel);
            CardLayout layout = (CardLayout) rightJPanel.getLayout();
            layout.next(rightJPanel);
        }
         
    
    }//GEN-LAST:event_btnUpdateFlightActionPerformed

    private void btnCancelFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelFlightActionPerformed
        // TODO add your handling code here:
         int selectedrow = tblFlightInfo.getSelectedRow();

        if(selectedrow >=0)
        {
            Flight vs = (Flight)tblFlightInfo.getValueAt(selectedrow,0);
            airliner.getFlightSchedule().getFlightList().remove(vs);
            JOptionPane.showMessageDialog(null, "Flight has been deleted");
            populate();
        }
        else
        JOptionPane.showMessageDialog(null,"Please select any row ");
    }//GEN-LAST:event_btnCancelFlightActionPerformed

